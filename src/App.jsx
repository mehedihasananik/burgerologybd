import "./App.css";

import { createBrowserRouter, RouterProvider } from "react-router-dom";

import { Layout, Home } from "../src/components";

const router = createBrowserRouter([
  {
    path: "/",
    element: <Layout />,
    children: [
      {
        path: "/",
        element: <Home />,
      },
    ],
  },
]);

function App() {
  return (
    <>
      <RouterProvider router={router} />
    </>
  );
}

export default App;
